def svg_open(fname, pnum, title):
    return HDR + '  <text x="60" y="38" font-family="Georgia, KaiTi, serif" font-size="22" fill="#FFFFFF">' + title + '</text>\n  <text x="1220" y="38" text-anchor="end" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="10" fill="#B2BEC3">' + pnum + '</text>\n'

def card_block(x, y, w, h, color, heading, lines):
    """Card with heading bar. Returns SVG string and next y position."""
    svg = ''
    svg += '  <rect x="' + str(x) + '" y="' + str(y) + '" width="' + str(w) + '" height="' + str(h) + '" rx="6" fill="#FFFFFF" stroke="' + color + '" stroke-width="1.5"/>\n'
    svg += '  <rect x="' + str(x) + '" y="' + str(y) + '" width="' + str(w) + '" height="32" rx="6" fill="' + color + '"/>\n'
    svg += '  <rect x="' + str(x) + '" y="' + str(y+24) + '" width="' + str(w) + '" height="8" fill="' + color + '"/>\n'
    svg += '  <text x="' + str(x+14) + '" y="' + str(y+22) + '" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="13" fill="#FFFFFF" font-weight="bold">' + heading + '</text>\n'
    yy = y + 48
    for line in lines:
        svg += '  <text x="' + str(x+14) + '" y="' + str(yy) + '" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="13" fill="#2D3436">' + line + '</text>\n'
        yy += 20
    return svg

def write_page(fname, pnum, title, source, body_svg):
    svg = svg_open(fname, pnum, title) + body_svg + FTR.format(source=source, pnum=pnum)
    with open(os.path.join(OUT, fname), 'w', encoding='utf-8') as f:
        f.write(svg)


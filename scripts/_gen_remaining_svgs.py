def make_page(fname, pnum, title, source, blocks):
    svg = HDR
    svg += '  <text x="60" y="40" font-family="Georgia, KaiTi, serif" font-size="23" fill="#FFFFFF">' + title + '</text>\n'
    svg += '  <text x="1220" y="40" text-anchor="end" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="10" fill="#B2BEC3">' + pnum + '</text>\n'
    for y, w, h, color, heading, lines in blocks:
        svg += '  <rect x="60" y="' + str(y) + '" width="' + str(w) + '" height="' + str(h) + '" rx="8" fill="#FFFFFF" stroke="' + color + '" stroke-width="1.5"/>\n'
        svg += '  <rect x="60" y="' + str(y) + '" width="' + str(w) + '" height="38" rx="8" fill="' + color + '"/>\n'
        svg += '  <rect x="60" y="' + str(y+30) + '" width="' + str(w) + '" height="8" fill="' + color + '"/>\n'
        svg += '  <text x="78" y="' + str(y+27) + '" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="14" fill="#FFFFFF" font-weight="bold">' + heading + '</text>\n'
        yy = y + 56
        for line in lines:
            svg += '  <text x="78" y="' + str(yy) + '" font-family="&quot;Microsoft YaHei&quot;, Arial, sans-serif" font-size="13" fill="#2D3436">' + line + '</text>\n'
            yy += 22
    svg += FTR.format(source=source, pnum=pnum)
    with open(os.path.join(OUT, fname), 'w', encoding='utf-8') as f:
        f.write(svg)

